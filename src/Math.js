// 수학 연산을 다루는 Math 객체
console.log(Math.abs(-5)); // 출력: 5 // x의 절대값을 반환
console.log(Math.ceil(1.1)); // 출력: 2 // x 이상의 가장 작은 정수를 반환
console.log(Math.floor(1.9)); // 출력: 1 // x 이하의 가장 큰 정수 반환
console.log(Math.round(1.4)); // 출력: 1 // x를 반올림한 정수를 반환
console.log(Math.max(1, 2, 3)); // 출력: 3
console.log(Math.min(1, 2, 3)); // 출력: 1
console.log(Math.pow(2, 3)); // 출력: 8 // x의 y제곱을 반환
console.log(Math.sqrt(9)); // 출력: 3 // x의 제곱근을 반환
console.log(Math.random()); // 출력: 0 이상 1 미만의 임의의 수

// 1 ~ 10까지의 임의의 수 생성
console.log(Math.floor(Math.random() * 10));
